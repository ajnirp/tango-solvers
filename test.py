    grid, constraints = testcase
    board = Board(grid=grid, constraints=constraints)
    solver = Solver()
    solve_result = solver.dfs_solve(board)
    if not solve_result:
        print(f"Testcase {idx+1}: could not solve")
        board.print()
    else:
        print(f"Testcase {idx+1}")
        solve_result.print()
    print()
